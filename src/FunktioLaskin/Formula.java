/*
 *Class used for DAO formulas
 */
public class Formula {

    private String formula = "";
    private String id;

    /*
    * Getters and setters
     */
    public String getFormula() {
        return formula;
    }

    public void setFormula(String formula) {
        this.formula = formula;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
